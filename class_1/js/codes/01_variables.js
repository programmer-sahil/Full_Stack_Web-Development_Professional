// 01 - Variables
// A variable is used to store data.
// We can use the stored data later in our program.

// Use let when the value may change later.
let studentName = "Madhu";

// Use const when the value should stay the same.
const courseName = "Full Stack Web Development";

// We can join text and variables using the + operator.
const message = studentName + " is learning " + courseName + ".";

// document.getElementById finds an HTML element using its id.
// textContent changes the text shown inside that element.
// document.getElementById("output").textContent = message;

// This also prints the message in the browser console.
console.log(message);
